    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int permisos_int, validacion = 0;
        String permisos_string = "", name_1, mail_1, p_number, username, password;

        name_1 = jTextField_nombre1.getText().trim();
        mail_1 = jTextField_mail.getText().trim();
        p_number = jTextField_phone.getText().trim();
        username = jTextField_username.getText().trim();
        password = jTextField_pass.getText().trim();

        permisos_int = jComboBox1.getSelectedIndex();

       

        if (name_1.equals("")) {

            jTextField_nombre1.setBackground(Color.PINK);
            validacion++;

        }

        if (mail_1.equals("")) {

            jTextField_mail.setBackground(Color.PINK);
            validacion++;

        }

        if (p_number.equals("")) {

            jTextField_phone.setBackground(Color.PINK);
            validacion++;

        }

        if (username.equals("")) {

            jTextField_username.setBackground(Color.PINK);
            validacion++;

        }

        if (password.equals("")) {

            jTextField_pass.setBackground(Color.PINK);
            validacion++;

        }

        switch (permisos_int) {
            case 1:
                permisos_string = "Administrador";
                break;
            case 2:
                permisos_string = "Asistente";
                break;
            case 3:
                permisos_string = "Tecnico";
                break;
            default:
                
                validacion++;
                break;
        }

        try {

            Connection cn_1 = get_connection.connect();
            PreparedStatement ps_1 = cn_1.prepareStatement(" select username from usuarios where username = '" + username + "'");

            ResultSet rs = ps_1.executeQuery();

            if (rs.next()) {

                jTextField_username.setBackground(Color.PINK);
                JOptionPane.showMessageDialog(null, "Nombre de usuario no disponible.");
                jTextField_username.setText("");
                cn_1.close();

            } else {

                cn_1.close();

                if (validacion == 0) {
                    try {
                        Connection cn_2 = get_connection.connect();
                        PreparedStatement ps_2 = cn_2.prepareStatement(
                                "insert into usuarios values (?,?,?,?,?,?,?,?,?)");

                        ps_2.setInt(1, 0);
                        ps_2.setString(2, jTextField_nombre1.getText());
                        ps_2.setString(3, jTextField_mail.getText());
                        ps_2.setString(4, jTextField_phone.getText());
                        ps_2.setString(5, jTextField_username.getText());
                        ps_2.setString(6, jTextField_pass.getText());
                        ps_2.setString(7, permisos_string);
                        ps_2.setString(8, "Activo");
                        ps_2.setString(9, Login.user);

                        ps_2.executeUpdate();
                        cn_2.close();
                        
                        

                        jTextField_nombre1.setBackground(new Color(51, 255, 51));
                        jTextField_mail.setBackground(new Color(51, 255, 51));
                        jTextField_phone.setBackground(new Color(51, 255, 51));
                        jTextField_username.setBackground(new Color(51, 255, 51));
                        jTextField_pass.setBackground(new Color(51, 255, 51));

                        JOptionPane.showMessageDialog(null, "Registro de usuario existoso.");

                        this.dispose();
                        
                        jTextField_nombre1.setBackground(new Color(153, 204, 255));
                        jTextField_mail.setBackground(new Color(153, 204, 255));
                        jTextField_phone.setBackground(new Color(153, 204, 255));
                        jTextField_username.setBackground(new Color(153, 204, 255));
                        jTextField_pass.setBackground(new Color(153, 204, 255));

                    } catch (HeadlessException | SQLException e) {
                        System.out.println("Error?" + e);
                    }

                }
                
                else{
                    
                    JOptionPane.showMessageDialog(null, "Llene todos los campos requeridos", "Advertencia", JOptionPane.ERROR_MESSAGE);
                    
                }

            }
        } catch (HeadlessException | SQLException e) {

        }

    }//GEN-LAST:event_jButton1ActionPerformed

